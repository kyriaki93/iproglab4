$("#create").click(function(){
    $("#divOfStartscreenness").hide();
    $("#selectDishView").show();
    $("#exampleView").show();
    $("#menuOverView").hide();
});

$("#confirm").click(function(){
    $("#divOfStartscreenness").hide();
    $("#selectDishView").hide();
    $("#exampleViewa").hide();
    $("#menuOverView").show();
});
 

function goBackBtn(){
    $("#divOfStartscreenness").hide();
    $("#selectDishView").show();
    $("#exampleViewa").show();
    $("#menuOverView").hide();
    $("#dishDetailView").hide();

}

function backBtn(){
    $("#divOfStartscreenness").hide();
    $("#selectDishView").show();
    $("#exampleViewa").show();
    $("#menuOverView").hide();
    $("#dishDetailView").hide();

}

function print(){
    $("#divOfStartscreenness").hide();
    $("#selectDishView").hide();
    $("#exampleViewa").hide();
    $("#menuOverView").hide();
    $("#dishDetailView").hide();

}